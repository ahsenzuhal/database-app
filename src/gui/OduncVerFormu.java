    private void btnOduncVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOduncVerActionPerformed
        // 1. Gerekli tüm bilgileri al
        String kitapID_str = txtKitapID.getText();
        String ogrenciID_str = txtOgrenciID.getText();
        String kitapAdi = txtKitapAdi.getText();
        String ogrenciAdi = txtOgrenciAdi.getText();
        String oduncTarihi = txtOduncTarihi.getText();
        String iadeTarihi = txtIadeTarihi.getText().trim(); // Kullanıcı girdiği için .trim() önemli

        // 2. Kapsamlı Kontrol: Gerekli alanlar dolu mu?
        //    (Kitap Adı ve Öğrenci Adı kutuları sadece "Ara" butonları başarılıysa doluyordu,
        //     bu yüzden onları kontrol etmek en önemlisi)
        if (kitapAdi.isEmpty() || ogrenciAdi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Lütfen geçerli bir Kitap ve Öğrenci seçmek için 'Ara' butonlarını kullanın!", "Eksik Bilgi", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (iadeTarihi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Lütfen bir iade tarihi girin! (YYYY-AA-GG formatında)", "Eksik Bilgi", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Basit tarih formatı kontrolü (daha karmaşık kontrol yapılabilir ama bu genelde yeterli)
        if (!iadeTarihi.matches("\\d{4}-\\d{2}-\\d{2}")) {
            JOptionPane.showMessageDialog(this, "İade tarihi formatı geçersiz! Lütfen YYYY-AA-GG formatında girin.", "Format Hatası", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 3. Veritabanı UPDATE işlemi
        Connection conn = null;
        PreparedStatement ps = null;
        // SQL: Kitabın durumunu, tarihlerini ve kimin aldığını güncelle
        String sql = "UPDATE kitaplar SET durum = ?, odunc_tarihi = ?, iade_tarihi = ?, odunc_alan_ogrenci_id = ? "
                   + "WHERE kitap_id = ? AND durum = 'Rafta'";

        try {
            conn = Baglanti.baglan();
            if (conn == null) {
                JOptionPane.showMessageDialog(this, "Veritabanı bağlantısı kurulamadı!", "Hata", JOptionPane.ERROR_MESSAGE);
                return;
            }

            ps = conn.prepareStatement(sql);
            ps.setString(1, "Ödünç Verildi");        // durum
            ps.setString(2, oduncTarihi);            // odunc_tarihi
            ps.setString(3, iadeTarihi);             // iade_tarihi
            ps.setInt(4, Integer.parseInt(ogrenciID_str)); // odunc_alan_ogrenci_id
            ps.setInt(5, Integer.parseInt(kitapID_str));   // kitap_id (WHERE şartı için)

            // 4. Sorguyu çalıştır
            int sonuc = ps.executeUpdate();

            if (sonuc > 0) {
                JOptionPane.showMessageDialog(this, "Kitap başarıyla " + ogrenciAdi + " adlı öğrenciye ödünç verildi!");

                // Tüm formu temizle
                txtKitapID.setText("");
                txtKitapAdi.setText("");
                txtOgrenciID.setText("");
                txtOgrenciAdi.setText("");
                txtIadeTarihi.setText("");
                // (Ödünç tarihini tekrar bugünün tarihiyle doldurmak için constructor'daki kodu buraya kopyalayabilirsin
                //  ama şimdilik temiz kalsın.)
                
            } else {
                // Bu durum, kitap ID'si doğru olsa bile 'Ara'ya bastıktan sonra
                // başka biri kitabı almışsa (veya durumu 'Rafta' değilse) oluşur.
                JOptionPane.showMessageDialog(this, "Kitap ödünç verilemedi. Kitabın durumunu kontrol edin (belki de 'Rafta' değil).", "İşlem Başarısız", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException e) {
            // MySQL'den tarih formatı hatası (örn: 2023-02-30) gelirse yakalarız
            if (e.getMessage().contains("Incorrect date value")) {
                JOptionPane.showMessageDialog(this, "İade tarihi geçersiz! (Örn: 30 Şubat). Lütfen YYYY-AA-GG formatında geçerli bir tarih girin.", "Geçersiz Tarih", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Veritabanı hatası: " + e.getMessage(), "Hata", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            // ID'ler sayı değilse (Bu 'Ara' butonlarında zaten kontrol ediliyor ama güvenlik için burada da durmalı)
            JOptionPane.showMessageDialog(this, "ID'ler sayısal olmalıdır!", "Format Hatası", JOptionPane.ERROR_MESSAGE);
        } finally {
            // 5. Bağlantıları her zaman kapat!
            try {
                if (ps != null) ps.close();
                if (conn != null) conn.close();
            } catch (SQLException e) {
                System.err.println("Kaynakları kapatırken hata: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnOduncVerActionPerformed

    private void btnOgrenciAraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOgrenciAraActionPerformed
        String ogrenciID_str = txtOgrenciID.getText().trim();

        if (ogrenciID_str.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Lütfen bir Öğrenci ID girin!", "Uyarı", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int ogrenciID;
        try {
            ogrenciID = Integer.parseInt(ogrenciID_str);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Öğrenci ID bir sayı olmalıdır!", "Hata", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Connection conn = null;
        PreparedStatement ps = null;
        ResultSet rs = null;
        String sql = "SELECT isim FROM ogrenciler WHERE ogrenci_id = ?";

        try {
            conn = Baglanti.baglan();
            if (conn == null) {
                JOptionPane.showMessageDialog(this, "Veritabanı bağlantısı kurulamadı!", "Hata", JOptionPane.ERROR_MESSAGE);
                return;
            }

            ps = conn.prepareStatement(sql);
            ps.setInt(1, ogrenciID);
            rs = ps.executeQuery();

            if (rs.next()) {
                String ogrenciAdi = rs.getString("isim");
                txtOgrenciAdi.setText(ogrenciAdi); // Öğrenci adını kutuya yaz
            } else {
                // Öğrenci bulunamadı
                JOptionPane.showMessageDialog(this, "Belirtilen Öğrenci ID'ye sahip bir öğrenci bulunamadı!", "Öğrenci Bulunamadı", JOptionPane.ERROR_MESSAGE);
                txtOgrenciID.setText("");
                txtOgrenciAdi.setText("");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Veritabanı hatası: " + e.getMessage(), "Hata", JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                if (rs != null) rs.close();
                if (ps != null) ps.close();
                if (conn != null) conn.close();
            } catch (SQLException e) {
                System.err.println("Kaynakları kapatırken hata: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnOgrenciAraActionPerformed

