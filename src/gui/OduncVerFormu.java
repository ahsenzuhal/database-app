    private void btnKitapAraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKitapAraActionPerformed
     String kitapID_str = txtKitapID.getText().trim();

        if (kitapID_str.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Lütfen bir Kitap ID girin!", "Uyarı", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int kitapID;
        try {
            kitapID = Integer.parseInt(kitapID_str);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Kitap ID bir sayı olmalıdır!", "Hata", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Connection conn = null;
        PreparedStatement ps = null;
        ResultSet rs = null;
        String sql = "SELECT kitap_adi, durum FROM kitaplar WHERE kitap_id = ?";

        try {
            conn = Baglanti.baglan();
            if (conn == null) {
                JOptionPane.showMessageDialog(this, "Veritabanı bağlantısı kurulamadı!", "Hata", JOptionPane.ERROR_MESSAGE);
                return;
            }

            ps = conn.prepareStatement(sql);
            ps.setInt(1, kitapID);
            rs = ps.executeQuery();

            if (rs.next()) {
                String kitapAdi = rs.getString("kitap_adi");
                String durum = rs.getString("durum");
                txtKitapAdi.setText(kitapAdi); // Kitap adını kutuya yaz

                if ("Rafta".equalsIgnoreCase(durum)) { // Durumu kontrol et
                    // Kitap raftaysa sorun yok
                } else {
                    JOptionPane.showMessageDialog(this, "Bu kitap şu an " + durum + " durumunda ve ödünç verilemez!", "Kitap Durumu", JOptionPane.WARNING_MESSAGE);
                    txtKitapID.setText(""); // ID'yi temizle
                    txtKitapAdi.setText(""); // Adı temizle
                }

            } else {
                // Kitap bulunamadı
                JOptionPane.showMessageDialog(this, "Belirtilen Kitap ID'ye sahip bir kitap bulunamadı!", "Kitap Bulunamadı", JOptionPane.ERROR_MESSAGE);
                txtKitapID.setText("");
                txtKitapAdi.setText("");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Veritabanı hatası: " + e.getMessage(), "Hata", JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                if (rs != null) rs.close();
                if (ps != null) ps.close();
                if (conn != null) conn.close();
            } catch (SQLException e) {
                System.err.println("Kaynakları kapatırken hata: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnKitapAraActionPerformed

